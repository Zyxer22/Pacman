	public static void resetInfluence(){
		for(int i = 0; i < influenceMap.length; i++){
			for(int j = 0; j < influenceMap[i].length; j++){
				if(influenceMap[i][j] > 0.)
					influenceMap[i][j] = 1.;
			}
		}
	}
	
	public float max(float first, float second){
		if(first > second)
			return first;
		else return second;
	}
	public float lerp(float a, float b, float percent){
		return (a + percent * (b-a));
	}
	public static double averageIt(int i, int j){
		double a = 0.5*(influenceMap[i+1][j]+influenceMap[i-1][j]
					+influenceMap[i][j+1]+influenceMap[i][j-1]);
		if(a > 1)
			return a;
		else return 1;
	}
